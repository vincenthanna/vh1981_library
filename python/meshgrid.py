import numpy as np

x = np.linspace(0, 3, 5)
y = np.linspace(0, 5, 4)
'''
x = [ 0.    0.75  1.5   2.25  3.  ] 5개
y = [ 0.    1.66666667  3.33333333  5.] 4개
'''

'''
meshgrid 명령은 사각형 영역을 구성하는 가로축의 점들과 세로축의 점을 나타내는
두 벡터를 인수로 받아 이 사각형 영역을 이루는 조합을 출력한다.
단 조합이 된 (x,y)쌍을 x값만을 표시하는 행렬과 y값만을 표시하는 행렬 두 개로 분리하여 출력한다.
'''
X, Y = np.meshgrid(x, y)
'''
[[ 0.    0.75  1.5   2.25  3.  ]
 [ 0.    0.75  1.5   2.25  3.  ]
 [ 0.    0.75  1.5   2.25  3.  ]
 [ 0.    0.75  1.5   2.25  3.  ]]
[[ 0.          0.          0.          0.          0.        ]
 [ 1.66666667  1.66666667  1.66666667  1.66666667  1.66666667]
 [ 3.33333333  3.33333333  3.33333333  3.33333333  3.33333333]
 [ 5.          5.          5.          5.          5.        ]]
'''

# flatten하게 펼쳐준다.
rX = X.ravel() #(20, )
rY = Y.ravel() #(20, )
'''
[ 0.    0.75  1.5   2.25  3.    0.    0.75  1.5   2.25  3.    0.    0.75
  1.5   2.25  3.    0.    0.75  1.5   2.25  3.  ]
[ 0.          0.          0.          0.          0.          1.66666667
  1.66666667  1.66666667  1.66666667  1.66666667  3.33333333  3.33333333
  3.33333333  3.33333333  3.33333333  5.          5.          5.          5.
  5.        ]
'''

# 열 기준으로 붙인다.(20) + (20) => (20, 2)
# (x,y)... 여러개 묶음
gridValue = np.c_[rX, rY] # (20, 2)
'''
[[ 0.          0.        ]
 [ 0.75        0.        ]
 [ 1.5         0.        ]
 [ 2.25        0.        ]
 [ 3.          0.        ]
 [ 0.          1.66666667]
 [ 0.75        1.66666667]
 [ 1.5         1.66666667]
 [ 2.25        1.66666667]
 [ 3.          1.66666667]
 [ 0.          3.33333333]
 [ 0.75        3.33333333]
 [ 1.5         3.33333333]
 [ 2.25        3.33333333]
 [ 3.          3.33333333]
 [ 0.          5.        ]
 [ 0.75        5.        ]
 [ 1.5         5.        ]
 [ 2.25        5.        ]
 [ 3.          5.        ]]
'''

print(gridValue)